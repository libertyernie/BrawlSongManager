		public class SongInfo {
			public SongInfo(FileInfo f) {
				File = f;
			}
			public SongInfo(string s) {
				File = new FileInfo(s + ".brstm");
			}

			public FileInfo File { get; private set; }

			public override string ToString() {
				string s = File.Name;
				if (File.Exists) {
					s = "* " + s;
				} else {
					s = "  " + s;
				}
				return s;
			}
		}

